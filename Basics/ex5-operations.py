# The basic operations Python supports in Python 3

# Addition (and subtraction)
print("Adding numbers")
print(1 + 2)
print(1 + 2.3)
print(1.1 + 1.2)

print("\nAdding booleans")
print(False)
print(False + False)
print(False + True)
print(True + True)
print(True + True + True)

print("\nMixing booleans and numbers")
print(True + True + 1.50)

print("\nAdding strings (but not subtracting)")
print("Hello" + "There")
print("the plus is" "optional")

# Multiplication
num = 36
print( -7 * num )
print("Jar" * 2)

# Division
print( -7 / num )
print(-7 // num )

# Modulus
print( (-7) % num )

# Exponentation
print(-2 ** 4 + 8 ** -4)
print((-2) ** 4 )